/** Map a country name (as stored on organizations.country) to a region. */
export function regionForCountry(country: string | null | undefined): Region | null {
    if (!country) return null;
    const c = country.trim();
    if (NORTH_AMERICA.has(c)) return "North America";
    if (SOUTH_AMERICA.has(c)) return "South America";
    if (EUROPE.has(c)) return "Europe";
    if (MENA.has(c)) return "MENA";
    if (SUB_SAHARAN_AFRICA.has(c)) return "Sub-Saharan Africa";
    if (ASIA.has(c)) return "Asia";
    if (OCEANIA.has(c)) return "Oceania";
    return null;
}
